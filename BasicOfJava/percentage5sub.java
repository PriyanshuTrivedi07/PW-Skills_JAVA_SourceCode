package BasicOfJava;

public class percentage5sub {
    public static void main(String[] args) {
        double maths = 89;
        double phy = 80;
        double chem = 93;
        double eng = 90;
        double hin = 97;
        double perc = (maths + phy + chem + eng + hin) / 5;
        System.out.println(perc+" %");

    }
}
